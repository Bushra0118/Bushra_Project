package com.javapolymorphism;

public class ClassA {

	public void Test1() {
System.out.println("this is my ClassA test one method");
	}
	public void Test1(int a) {
	System.out.println("this is my ClassA test one method=" +a);
	}
	public void Test1(int a,int b) {
		System.out.println("this is my ClassA test one method=" +a+","+b);
	}
	
	public void Test1(String a,int b) {
		System.out.println("this is my ClassA test one method=" +a+","+b);
	}

}

